    function getIMG($fields,$where='',$url=false,$one=false,$array='array'){
        
        $this->db->select($fields);
        $this->db->from('imagem_produto');
        if($where){
            $this->db->where($where);
        }
        
        $query = $this->db->get();
        
        $result =  !$one  ? $query->result() : $query->row();

        if($url == true){
            foreach ($result as $value) {

                $final[] = $value->url;

            }

